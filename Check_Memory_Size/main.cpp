#include <iostream>

using namespace std;

int main()
{
    int x;
    short sh;
    char ch;
    float f;
    double d;
    long l;
    bool boolean;
    string s;


    cout<<"The Memory Size Of Boolean Data Type: "<<sizeof(boolean)<<endl;
    cout<<"The Memory Size Of Character Data Type: "<<sizeof(ch)<<endl;
    cout<<"The Memory Size Of Short Data Type: "<<sizeof(sh)<<endl;
    cout<<"The Memory Size Of Integer Data Type: "<<sizeof(x)<<endl;
    cout<<"The Memory Size Of Float Data Type: "<<sizeof(f)<<endl;
    cout<<"The Memory Size Of Long Data Type: "<<sizeof(l)<<endl;
    cout<<"The Memory Size Of Double Data Type: "<<sizeof(d)<<endl;
    cout<<"The Memory Size Of String Data Type: "<<sizeof(s)<<endl;

    return 0;
}
